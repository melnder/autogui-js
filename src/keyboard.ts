export function press(key: Key | string): void {
  const vkCode = typeof key === "string" ? VK[key.toLowerCase()] : key
  if (vkCode === undefined) {
    throw new Error(`Unsupported key: ${key} (${vkCode})`)
  }
  native.press(vkCode)
}
